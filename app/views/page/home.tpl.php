
<p>
  <strong>Bienvenido al Registro Público Provincial de Villas y Asentamientos !</strong>
</p>
<p>
  Esta aplicación constituye una herramienta que integra requerimientos de tres Direcciones Provinciales de la Subsecretaría Social de Tierra Urbanismo y Vivienda, permitiendo el seguimiento y la administración de manera centralizada de la información de los barrios que componen el Registro Público Provincial de Villas y Asentamientos Precarios (Ley 14.449), las gestiones realizadas en el marco del Área de Conflictos Habitacionales con el objetivo de suspender los desalojos y las intervenciones realizadas por la Dirección de Regularización Urbana y Dominial.
</p>
<p>
El sistema funciona con distintos perfiles de usuarios a los con distintos permisos de visualización o edición de la información y se compone básicamente de una guía de barrio que se articula con un sistema de información geográfica, que facilita la identificación catastral y dominial de las parcelas que componen cada barrio. A su vez, cada uno de los barrios posee un identificador único que es un número de Folio (equivalente al DNI de una persona). Por medio de este número no sólo es posible disponer de un repositorio de información centralizada para almacenar fotos, planos, informes, resoluciones y cualquier otro tipo documentación, sino acceder siete solapas temáticas para cada barrio:
</p>

<p>
  <ul>
    <li> Datos Generales del Barrio </li>

    <li> Nomenclatura Catastral y Dominio</li>

    <li> Condiciones Socio-Urbanísticas</li>

    <li> Regularización e Integración socio-urbana</li>

    <li> Conflictos y Judicialización</li>

    <li> Censo y Adjudicación</li>

    <li> Comentarios</li>
  </ul>
</p>

<p>
  Ésta última funcionalidad (Comentarios) se encuentra orientada a facilitar la comunicación entre las distintas áreas de la Subsecretaría acerca del seguimiento de la acciones en un mismo barrio y, al mismo tiempo, permitir documentar todo aquello que el sistema no habilita a través del modelo de datos estructurado.
</p>



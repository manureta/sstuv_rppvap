    consignan:------------------------------------------------------------------------------------------------------------------------------------------------------------
</p>
<p align="justify">
    <strong>PRIMERA</strong>
    :”EL INSTITUTO” vende al “EL COMPRADOR” y este compra con ajuste a las disposiciones precedentemente mencionadas y en las condiciones que se pactan en el
    presente, el inmueble identificado en el encabezamiento de este Boleto.
</p>
<p align="justify">
    La venta del inmueble se formaliza en el estado y en las condiciones en que se encuentra a la fecha del presente que “EL COMPRADOR” declara conocer y
    aceptar y sin reserva alguna con renuncia expresa a interponer cualquier acción presente o futura contra “EL INSTITUTO” y/o “PROVINCIA DE BUENOS AIRES”,
    por cualquier causa o motivo quedando a su exclusivo cargo los gastos que por cualquier concepto pudieran originarse en lo sucesivo, tomando posesión del
    inmueble de plena conformidad en este acto y exonerando de Responsabilidad al INSTITUTO o a la PROVINCIA por eventuales reclamos que pudieran hacer los
    terceros que funden sus derechos en anteriores Boletos otorgados y/o rescindidos, (arts. 2098, 2100,2101, 2.106 y concordantes del C.Civil).En tal sentido
    declaro conocer la situación del inmueble que se me adjudica y/o que resulto sucesor a titulo individual o universal del anterior adjudicatario, sirviendo
    el presente de suficiente constancia.------------------------------------------------------------------------------------------
</p>

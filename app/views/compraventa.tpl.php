<p align="justify">
    La falta de pago de tres servicios de amortización consecutivos o como máximo cinco alternados por año calendario hará exigible el saldo total adeudado,
    sin necesidad de interpelación judicial o extrajudicial alguna. En el supuesto en que la mora del deudor se produjera antes de otorgarse la escritura
    traslativa de dominio, “EL INSTITUTO” podrá optar entre exigir el pago del saldo total adeudado dentro de un plazo perentorio e impostergable que no
    excederá de TREINTA (30) días corridos o bien declarar la rescisión del boleto, con perdida para el comprador de todas las sumas abonadas, que quedarán a
    favor de “ EL INSTITUTO” en concepto de indemnización por el uso del bien sin perjuicio de exigir desde entonces el reintegro de la posesión del inmueble.
    Se considerarán los importes actualizados que hubieran abonado como el pago por el uso del inmueble el que se determinará conforme al valor locativo del
    mismo al momento de su rescisión. En el supuesto de resultar saldo a favor del comprador una vez deducidos los rubros indicados, deberá “EL INSTITUTO”,
    devolverlo actualizado al comprador; caso contrario, de existir deuda a favor de “EL INSTITUTO”, éste podrá exigir su cobro judicialmente.
</p>
<p align="justify">
    El pacto comisorio por falta de pago no podrá hacerse valer después que el adquirente haya abonado el 25% del precio actualizado, o haya realizado
    construcciones equivalentes al 50% del precio de compra, ambas actualizadas.
</p>
<p align="justify">
    Si la mora se produjera después de constituida la hipoteca, EL acreedor procederá a la ejecución del crédito con ajuste a las cláusulas de la
    hipoteca.------------------------------------
</p>
<p align="justify">
    <strong>NOVENA:</strong>
    En la cancelación anticipada de la deuda, el saldo se determinará multiplicando el número de cuotas faltantes por el valor de ésta según el último
    vencimiento. De igual modo, cuando los adjudicatarios desearen anticipar pagos de cuotas, los importes respectivos se aplicarán de acuerdo al párrafo
    anterior.
</p>
<p align="justify">
    El importe respectivo se pondrá a disposición de los interesados a requerimiento de estos. A su vez, el pago parcial anticipado implicará la automática
    suspensión de pagos posteriores, hasta la recepción de los nuevos elementos de pago que proveerá “LA
    SUBSECRETARIA”---------------------------------------------------------------------------------------------
</p>
<p align="justify">
    <strong>DECIMA</strong>
    : Toda transgresión a las normas establecidas por “El INSTITUTO”, “LA SUBSECRETARIA” y/o “EL PROGRAMA”, para la adjudicación de inmuebles facultará al
    “INSTITUTO” a dar por rescindido el presente boleto de pleno derecho y sin necesidad de interpelación judicial o extrajudicial alguna, debiendo procederse
    a su inmediato desalojo sean quienes fueren sus ocupantes salvo si están cumplidos los porcentajes de la cláusula octava.
</p>
<p align="justify">
    La perdida de los importes que hubiere abonado, será total para el comprador, los que quedarán a favor de “EL INSTITUTO”, en concepto de indemnización por
    el uso del inmueble, a cuyo fin se aplicará el procedimiento previsto en el artículo 8° segundo párrafo, sin perjuicio de perseguir el cobro de las sumas
    que adeudare el comprador al momento de disponerse la rescisión.----------------------------------------------------------------------
</p>
